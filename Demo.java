public class Demo {
	public static void main(String[] args)
	{
		// byte type
		byte b = 20;
		System.out.println("b= " + b);

		// short type
		short s = 20;
		System.out.println("s= " + s);

		// int type
		int i = 20;
		System.out.println("i= " + i);

		// long type
		long l = 20;
		System.out.println("l= " + l);
	}
}